import Vue from 'vue'

<template>
    <div v-if="task.name" class="taskDetail">
        <div v-if="task.name" class="editTask">
            <div v-show="!editingTaskName" class="taskNameToEdit">
                <b>{{ task.name }}</b>
                <font-awesome-icon v-show="!editingTaskName" @click="startEditTaskName" icon="pencil-alt"
                                   class="iconTaskNameToEdit"/>
            </div>
            <input v-show="editingTaskName"
                   type="text"
                   class="inputTaskNameToEdit"
                   v-model="task.name"
                   @blur="doneEditTaskName"
                   @keyup.enter="doneEditTaskName"
                   @keyup.esc="cancelEditTaskName"
                   @focus="$event.target.select()"
                   v-focus="editingTaskName"/>

        </div>

        <div v-if="task.name" class="whenChoices">
            <a @click="setWhen('today')" :class="{'is-active': task.when === 'today'}" class="whenChoice">
                today</a>
            <a @click="setWhen('thisweek')" :class="{'is-active': task.when === 'thisweek'}" class="whenChoice">
                this week</a>
            <a @click="setWhen('waitingfor')" :class="{'is-active': task.when === 'waitingfor'}" class="whenChoice">
                waiting for</a>
            <a @click="setWhen('someday')" :class="{'is-active': task.when === 'someday'}" class="whenChoice">
                someday</a>
        </div>

        <button v-if="task.name" v-show="!editingNote && !task.note" class="is-active addNote" @click="startEditNote">
            add note
        </button>

        <div v-show="editingNote" class="controlsLine">
            <div class="editNoteControls">
                <button @click="doneEditNote" class="is-active">save</button>
                <button @click="cancelEditNote">cancel</button>
                <button @click="deleteEditNote">delete</button>
            </div>
        </div>
        <div v-show="!editingNote" class="controlsLine">
            <div class="editNoteControls">
                <button v-show="task.note" class="is-active" @click="startEditNote">
                    edit note
                </button>
            </div>
        </div>

        <div class="taskNote" v-if="task.name">
                <div v-show="!editingNote" class="noteDisplay" v-html="markedNote"></div>
                <div v-show="editingNote">
                    <textarea class="noteInput" placeholder="add your note here" v-model="task.note"></textarea>
                </div>
        </div>
    </div>
</template>

<script>
  import FontAwesomeIcon from '@fortawesome/vue-fontawesome'

  // Markdown transformer
  let marked = require('marked')
  let renderer = new marked.Renderer()

  // Make all links generated by marked open in new window
  renderer.link = function (href, title, text) {
    return '<a target="_blank" href="' + href + '" title="' + title + '">' + text + '</a>'
  }

  export default {
    name: 'TaskDetail',
    components: {FontAwesomeIcon},
    props: {
      task: {
        type: Object,
        required: true
      }
    },
    data: function () {
      return {
        editingTaskName: false,
        editingNote: false
      }
    },
    computed: {
      selectedProject: function () {
        return this.$store.getters.getSelectedProject
      },
      selectedProjectName: function () {
        return this.$store.getters.getSelectedProjectName
      },
      markedNote: function () {
        if (this.task.note) {
          return marked(this.task.note, {renderer: renderer})
        }
      }
    },
    filters: {
      prettyWhen: function (n) {
        if (n === 'today') {
          return 'Today'
        }
        if (n === 'thisweek') {
          return 'This Week'
        }
        if (n === 'waitingfor') {
          return 'Waiting For'
        }
        if (n === 'someday') {
          return 'Someday'
        }
      }
    },
    methods: {
      setWhen: function (when) {
        console.log('setting task to when: ' + when)
        this.task.when = when
        this.saveTask(this.task)
      },
      startEditTaskName: function () {
        console.log('starting to edit task name: ' + this.task.name)
        this.taskNameBeforeEdit = this.task.name
        this.editingTaskName = true
      },
      doneEditTaskName: function () {
        if (!this.editingTaskName) {
          return
        }
        this.task.name = this.task.name.trim()
        if (!this.task.name) {
          this.cancelEditTaskName()
        }
        this.editingTaskName = false
        this.taskNameBeforeEdit = null
        this.saveTask(this.task)
      },
      cancelEditTaskName: function () {
        console.log('text before editing: ' + this.taskNameBeforeEdit)
        this.task.name = this.taskNameBeforeEdit
        this.taskNameBeforeEdit = null
        this.editingTaskName = false
      },
      startEditNote: function () {
        console.log('starting to edit note')
        this.noteBeforeEdit = this.task.note
        this.editingNote = true
      },
      doneEditNote: function () {
        console.log('done to edit note')
        this.editingNote = false
        this.noteBeforeEdit = null
        this.saveTask(this.task)
      },
      cancelEditNote: function () {
        console.log('cancel edit note')
        this.task.note = this.noteBeforeEdit
        this.noteBeforeEdit = null
        this.editingNote = false
      },
      deleteEditNote: function () {
        console.log('delete edit note')
        this.task.note = null
        this.saveTask(this.task)
        this.editingNote = false
      },
      saveTask: function (task) {
        this.$store.commit('setSelectedTask', this.task)
        // Also save this updated task in the task list
        this.$emit('updateTask', this.task)
      }
    },
    // https://vuejs.org/v2/guide/custom-directive.html
    directives: {
      focus: function (el, binding) {
        if (binding.value) {
          el.focus()
        }
      }
    }
  }
</script>

