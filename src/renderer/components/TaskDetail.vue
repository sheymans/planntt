import Vue from 'vue'

<template>
    <div v-if="Object.keys(task).length !== 0" class="taskDetail">
        <div v-if="Object.keys(task).length !== 0" class="editTask">
            <div class="editTaskFirstPart">
                <div v-show="!editingTaskName" class="taskNameToEdit">
                    <font-awesome-icon v-tipster="'close detail (ESC)'" @click="closeDetail" icon="times" class="iconCloseDetail"/>
                    <b>{{ task.name }}</b>
                    <font-awesome-icon v-tipster="'edit task name'"
                                       @click="startEditTaskName" icon="pencil-alt"
                                       class="iconTaskNameToEdit"/>
                </div>
                <input v-show="editingTaskName"
                       type="text"
                       class="inputTaskNameToEdit"
                       v-model="task.name"
                       @blur="doneEditTaskName"
                       @keyup.enter="doneEditTaskName"
                       @keyup.esc="cancelEditTaskName"
                       @focus="$event.target.select()"
                       v-focus="editingTaskName"/>
            </div>
            <div class="taskIcons">
                <font-awesome-icon v-tipster="'move task up (ctrl+⌘+↑)'" @click="moveUp" icon="arrow-up"/>
                <font-awesome-icon v-tipster="'move task down (ctrl+⌘+↓)'" @click="moveDown" icon="arrow-down"/>
                <font-awesome-icon v-tipster="'duplicate task (⌘+d)'" @click="duplicateTask" icon="clone" class="iconDuplicateTask"/>
                <font-awesome-icon v-tipster="'(un)block task'" @click="blockTask" icon="ban" :class="{'blocked': blocked, 'notBlocked': !blocked}"/>
                <font-awesome-icon v-tipster="'show project (⌘+p)'" @click="expandProject" icon="crosshairs"/>
                <router-link class="focusTask" :to="{ name: 'focusTask', params: { task } }"><font-awesome-icon v-tipster="'focus mode (⌘+f)'" icon="headphones"/></router-link>
            </div>
        </div>

        <div v-if="Object.keys(task).length !== 0" class="whenChoices">
            <a @click="setWhen('today')" :class="{'is-active': task.when === 'today'}" class="whenChoice whenChoiceToday">
                today</a>
            <a @click="setWhen('thisweek')" :class="{'is-active': task.when === 'thisweek'}" class="whenChoice whenChoiceThisWeek">
                this week</a>
            <a @click="setWhen('waitingfor')" :class="{'is-active': task.when === 'waitingfor'}" class="whenChoice whenChoiceWaitingFor">
                waiting for</a>
            <a @click="setWhen('someday')" :class="{'is-active': task.when === 'someday'}" class="whenChoice whenChoiceSomeday">
                someday</a>
            <date-picker class="whenChoiceSpecific" lang="en" @change="changeDate" placeholder="due date" v-model="task.due" :first-day-of-week="1"></date-picker>
        </div>

        <button v-if="Object.keys(task).length !== 0" v-show="!editingNote && !task.note" class="is-active addNote" @click="startEditNote">
            add note
        </button>

        <div v-show="editingNote" class="controlsLine">
            <div class="editNoteControls">
                <button @click="doneEditNote" class="is-active">save</button>
                <button @click="cancelEditNote">cancel</button>
                <button @click="deleteEditNote">delete</button>
            </div>
        </div>
        <div v-show="!editingNote" class="controlsLine">
            <div class="editNoteControls">
                <button v-show="task.note" class="is-active" @click="startEditNote">
                    edit note
                </button>
            </div>
        </div>

        <div class="taskNote" v-if="Object.keys(task).length !== 0">
                <div v-show="!editingNote" class="noteDisplay" v-html="markedNote"></div>
                <div v-show="editingNote">
                    <textarea class="noteInput" placeholder="add your note here" v-model="task.note"></textarea>
                </div>
        </div>
        <div class="timers">#sessions: {{numberOfSessions}} | total time: {{prettyTotalTimeSpent}}  | created: {{ task.created | moment("YYYY-MM-DD hh:mma")}}
        </div>
    </div>
</template>

<script>
import { FontAwesomeIcon } from '@fortawesome/vue-fontawesome'
import DatePicker from 'vue2-datepicker'
import 'vue2-datepicker/index.css'

// Markdown transformer
const marked = require('marked')
const renderer = new marked.Renderer()

// Make all links generated by marked open in new window
renderer.link = function (href, title, text) {
  return '<a target="_blank" href="' + href + '" title="' + href + '">' + text + '</a>'
}

export default {
  name: 'TaskDetail',
  components: { FontAwesomeIcon, DatePicker },
  props: {
    task: {
      type: Object,
      required: true
    }
  },
  data: function () {
    return {
      editingTaskName: false
    }
  },
  computed: {
    editingNote: function () {
      return this.$store.getters.isEditingNote
    },
    blocked: function () {
      return !!this.task.blocked
    },
    selectedProject: function () {
      return this.$store.getters.getSelectedProject
    },
    selectedProjectName: function () {
      return this.$store.getters.getSelectedProjectName
    },
    prettyTotalTimeSpent: function () {
      let totalTimeSpent = 0
      if (this.task.totalTimeSpent) {
        totalTimeSpent = this.task.totalTimeSpent
      }
      const hours = Math.floor(totalTimeSpent / 3600)
      const minutes = Math.floor((totalTimeSpent - (hours * 3600)) / 60)
      const seconds = totalTimeSpent - (hours * 3600) - (minutes * 60)
      return hours + 'h:' + minutes + 'm:' + seconds + 's'
    },
    numberOfSessions: function () {
      if (this.task.numberOfSessions) {
        return this.task.numberOfSessions
      } else {
        return 0
      }
    },
    markedNote: function () {
      if (this.task.note) {
        return marked(this.task.note, { renderer: renderer })
      }
    }
  },
  filters: {
    prettyWhen: function (n) {
      if (n === 'today') {
        return 'Today'
      }
      if (n === 'thisweek') {
        return 'This Week'
      }
      if (n === 'waitingfor') {
        return 'Waiting For'
      }
      if (n === 'someday') {
        return 'Someday'
      }
    }
  },
  methods: {
    expandProject: function () {
      this.$store.commit('setPathFromRootToProjectExpanded', this.task.project)
    },
    averageTimePerSession: function () {
      if (this.numberOfSessions === 0) {
        return 0
      } else {
        return this.totalTimeSpent / this.numberOfSessions
      }
    },
    changeDate: function (currentValue) {
      this.saveTask()
    },
    setWhen: function (when) {
      this.$emit('setWhen', this.task, when)
    },
    duplicateTask: function () {
      this.$emit('duplicateTask', this.task)
    },
    moveUp: function () {
      this.$emit('moveUp', this.task)
    },
    moveDown: function () {
      this.$emit('moveDown', this.task)
    },
    closeDetail: function () {
      this.$emit('closeDetail', this.task)
    },
    blockTask: function () {
      const currentBlock = this.task.blocked
      if (currentBlock) {
        console.log('unblocking task: ' + this.task.name)
        this.$set(this.task, 'blocked', false)
      } else {
        console.log('blocking task: ' + this.task.name)
        this.$set(this.task, 'blocked', true)
      }
      this.saveTask()
    },
    startEditTaskName: function () {
      console.log('starting to edit task name: ' + this.task.name)
      this.taskNameBeforeEdit = this.task.name
      this.editingTaskName = true
    },
    doneEditTaskName: function () {
      if (!this.editingTaskName) {
        return
      }
      this.task.name = this.task.name.trim()
      if (!this.task.name) {
        this.cancelEditTaskName()
      }
      this.editingTaskName = false
      this.taskNameBeforeEdit = null
      this.saveTask()
    },
    cancelEditTaskName: function () {
      console.log('text before editing: ' + this.taskNameBeforeEdit)
      this.task.name = this.taskNameBeforeEdit
      this.taskNameBeforeEdit = null
      this.editingTaskName = false
    },
    startEditNote: function () {
      console.log('starting to edit note')
      this.noteBeforeEdit = this.task.note
      this.$store.commit('setEditingNote', true)
    },
    doneEditNote: function () {
      console.log('done to edit note')
      this.$store.commit('setEditingNote', false)
      this.noteBeforeEdit = null
      this.saveTask()
    },
    cancelEditNote: function () {
      console.log('cancel edit note')
      this.task.note = this.noteBeforeEdit
      this.noteBeforeEdit = null
      this.$store.commit('setEditingNote', false)
    },
    deleteEditNote: function () {
      console.log('delete edit note')
      this.task.note = null
      this.saveTask()
      this.$store.commit('setEditingNote', false)
    },
    saveTask: function () {
      this.$taskDb.update({ id: this.task.id }, this.task, {})
    },
    // https://stackoverflow.com/questions/105034/create-guid-uuid-in-javascript
    uuidv4: function () {
      return 'xxxxxxxx-xxxx-4xxx-yxxx-xxxxxxxxxxxx'.replace(/[xy]/g, function (c) {
        const r = Math.random() * 16 | 0
        const v = c === 'x' ? r : (r & 0x3 | 0x8)
        return v.toString(16)
      })
    }
  },
  // https://vuejs.org/v2/guide/custom-directive.html
  directives: {
    focus: function (el, binding) {
      if (binding.value) {
        el.focus()
      }
    }
  }
}
</script>

